import numpy as np
import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt
from sklearn.preprocessing import StandardScaler
from sklearn.model_selection import train_test_split
from sklearn.svm import SVC
from sklearn.tree import DecisionTreeClassifier
from sklearn.linear_model import LogisticRegression
from sklearn.neighbors import KNeighborsClassifier
from sklearn.naive_bayes import GaussianNB
from sklearn.metrics import accuracy_score,f1_score,classification_report

# LOADING THE DATA
df = pd.read_csv('/dataset.csv')
df

df.head()

df.describe()

df.shape

df.isnull().sum()

df.duplicated().sum()

df.info()

# Here 0 means NON-DIABETEIC and 1 means DIABETEIC
counts=df['Outcome'].value_counts()
counts

#plotiing histogram
z=df.hist(figsize=(20,20))

df['Outcome'].value_counts().plot(kind="bar", title='Distribution of Diabetes Outcomes')
plt.xticks(rotation=0)

plt.pie(counts,
        labels=counts.index,
        autopct='%1.0f%%',
        colors=['salmon','lightgreen'],
        startangle=140)
plt.title('Distribution of Diabetes Outcomes')
plt.show()

df.groupby('Outcome').mean()

# SEPARATING THE DATA
X = df.drop(columns='Outcome',axis=1)
Y = df['Outcome']

print(X)
print(Y)

# DATA STANDARDIZATION
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

print(X_scaled)
print(Y)

# SPLITTING DATA INTO TRAINING AND TESTING DATA
X_train,X_test,Y_train,Y_test = train_test_split(X_scaled,Y,test_size=0.2,stratify=Y,random_state=2)

print(X_scaled.shape,X_train.shape,X_test.shape)

# TRAINING THE MODEL
model = SVC(kernel='linear')

model.fit(X_train,Y_train)

# evalaution of model

Y_train_prediction = model.predict(X_train)
training_data_accuracy = accuracy_score(Y_train_prediction,Y_train)

print('Accuracy score of the train data : ', training_data_accuracy)

Y_test_prediction = model.predict(X_test)
testing_data_accuracy = accuracy_score(Y_test_prediction,Y_test)

print('Accuracy score of the test data : ', testing_data_accuracy)

# inputing our own data

input_data = (8,99,84,0,0,35.4,0.388,50)
input_data_array = np.asarray(input_data)

#reshape array
input_data_reshaped = input_data_array.reshape(1,-1)

# Standardize the new given data.
std_input_data = scaler.transform(input_data_reshaped)
print(std_input_data)

prediction = model.predict(std_input_data)
print(prediction)

if prediction[0] == 0:
    print("✅ No Diabetes")
else:
    print("⚠️ Diabetes Detected")

models={
    "Logistic Regression": LogisticRegression(),
    "KNN" : KNeighborsClassifier(),
    "Decision Tree" : DecisionTreeClassifier(),
    "Naive Bayes" : GaussianNB(),
    "Support Vector Machine" : SVC(kernel='linear')
}

result=[]

for name, model in models.items():
  model.fit(X_train,Y_train)
  y_pred=model.predict(X_test)
  acc=accuracy_score(Y_test,y_pred)
  f1=f1_score(Y_test,y_pred)
  result.append({
      'model':name,
      'accuracy':round(acc,4),
      'f1 score' : round(f1,4)
  })

result

import joblib
joblib.dump(models [ 'Support Vector Machine' ], 'SVM_diabetes.pkl')
joblib.dump(scaler, 'diabetes_scaler.pkl')
joblib.dump(X.columns.tolist(), 'diabetes_columns.pkl')

